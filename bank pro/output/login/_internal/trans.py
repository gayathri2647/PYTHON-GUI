    def find(self):
        a=self.le1.text()
        db=sqlite3.connect("bank.db")
        s="select * from customer where accno="+a+";"
        r=db.execute(s)
        rec=r.fetchall()

        if len(rec)==0:
            self.le2.clear()
            QMessageBox.critical(self.form5,"Invalid Account","Not found")
        else:
            self.le2.setText(str(rec[0][1]))
    def save(self):
        a=self.le1.text()
        b=self.le2.text()
        c=self.de1.date().toString("dd/MM/yyyy")
        d=self.cb1.currentText()
        e=int(self.le3.text())
        db=sqlite3.connect("bank.db")
        s="insert into trans(accno,name,dot,ttype,tamount) values("+a+",'"+b+"','"+c+"','"+d+"',"+str(e)+");"
        db.execute(s)
        db.commit()
        if d=="Deposit":
            m="update customer set balance=balance + "+str(e)+" where accno="+a+";"
        else:
            m="update customer set balance=balance - "+str(e)+" where accno="+a+";"
        db.execute(m)
        db.commit()
        db.close()
        QMessageBox.about(self.form5,"ADDING TRANSACTION","Transaction Successful")
        self.le1.clear()
        self.le2.clear()
        self.le3.clear()

